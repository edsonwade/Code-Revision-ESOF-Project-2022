import React from 'react';
import { clsx, type ClassValue } from 'clsx';
import { twMerge } from 'tailwind-merge';

function cn(...inputs: ClassValue[]) {
    return twMerge(clsx(inputs));
}

interface StatusBadgeProps {
    status: string;
    className?: string;
}

const statusConfig: Record<string, { label: string; color: string }> = {
    // Appointment Status
    SCHEDULED: { label: 'Scheduled', color: 'bg-blue-100 text-blue-700 border-blue-200' },
    COMPLETED: { label: 'Completed', color: 'bg-green-100 text-green-700 border-green-200' },
    CANCELLED: { label: 'Cancelled', color: 'bg-red-100 text-red-700 border-red-200' },
    RESCHEDULED: { label: 'Rescheduled', color: 'bg-purple-100 text-purple-700 border-purple-200' },
    NO_SHOW: { label: 'No Show', color: 'bg-gray-100 text-gray-700 border-gray-200' },
    // Roles
    SUPER_ADMIN: { label: 'Super Admin', color: 'bg-indigo-100 text-indigo-700 border-indigo-200' },
    ADMIN: { label: 'Admin', color: 'bg-slate-100 text-slate-700 border-slate-200' },
    EXPLAINER: { label: 'Explainer', color: 'bg-amber-100 text-amber-700 border-amber-200' },
    STUDENT: { label: 'Student', color: 'bg-emerald-100 text-emerald-700 border-emerald-200' },
};

export const StatusBadge: React.FC<StatusBadgeProps> = ({ status, className }) => {
    const config = statusConfig[status] || { label: status, color: 'bg-gray-100 text-gray-600 border-gray-200' };

    return (
        <span
            className={cn(
                'inline-flex items-center px-2.5 py-0.5 rounded-full text-xs font-medium border transition-all duration-200',
                config.color,
                className
            )}
        >
            {config.label}
        </span>
    );
};
